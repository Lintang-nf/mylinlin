print('Menghitung Luas dan keliling Trapesium') 
 
sisi_atas = float(input('Input panjang sisi atas: ')) 
sisi_bawah = float(input('Input panjang sisi bawah: ')) 
tinggi = float(input('Input tinggi trapesium: ')) 
sisi_1 = float(input('Input sisi 1: ')) 
sisi_2 = float(input('Input sisi 2: ')) 
sisi_3 = float(input('Input sisi 3: ')) 
sisi_3 = float(input('Input sisi 4: ')) 
 
luas = 0.5 * (sisi_atas + sisi_bawah) * tinggi 
keliling = sisi_1 + sisi_2 + sisi_3 + sisi_3 
 
print('Luas trapesium =', luas) 
print('Keliling trapesium =', keliling)
